public class SumOfElementOfArray {
    public static void main(String[] args) {
        int arr[]={10,20,30,40,50};
        int i=0;
        int sum=0;
        while(i<arr.length){
            int int1=(arr[i]);
            sum=sum+int1;
            i++;
        }
        System.out.println(sum);
    }
    
}
